#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	size_t	len;

	len = 0;
	if (lst)
	{
		while (lst->next != 0)
		{
			len++;
			lst = lst->next;
		}
		len++;
	}
	return (len);
}
