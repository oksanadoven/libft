#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	unsigned	i;

	i = 0;
	while (src[i] != 0)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
